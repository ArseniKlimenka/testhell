update 
    ACC.GL_ADDITIONAL_ATTRIBUTES
set 
    GL_ADDITIONAL_ATTRIBUTES.TRANSACTION_TYPE_ID = 5
from 
    ACC.GL_ADDITIONAL_ATTRIBUTES aa
    inner join acc.GL_SUBLEDGER_ENTRY s on aa.GL_ADDITIONAL_ATTRIBUTE_ID = s.GL_ADDITIONAL_ATTRIBUTE_ID
where aa.TRANSACTION_TYPE_ID = 4
  and s.POSTING_SCHEME_ID = 60
  and s.DOCUMENT_TYPE_ID = 1009;